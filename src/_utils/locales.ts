export const isSupportedDateLocales = (
  locale: SupportedDateLocales | undefined
) => {
  return includes(supportedDateLocalesArray, locale)
}

export const throwUnsupportedDateLocaleError = (
  locale: SupportedDateLocales | undefined
) => {
  invariant(
    isSupportedDateLocales(locale),
    `${locale} is not a supported locale\n The following locales are supported ${supportedDateLocalesArray.join(
      ', '
    )}`
  )
}
